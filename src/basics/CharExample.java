package basics;

public class CharExample {
    public static void main(String[] args) {
        char ch = 'M';
        System.out.println(ch);

        ch++; // will give out n
        System.out.println(ch);

        //convert from char to int
        System.out.println((int) ch);

        //convert from int to ch
        System.out.println((char) 122);
    }
}
